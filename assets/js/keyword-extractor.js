export const words = {};

/**
 * Used to parse a string and extract keywords and number of occurences
 * Uses the words constant
 *
 * @param {String} phrase The string to parse
 */
export function recordPhrase(phrase) {
	let xs = phrase.normalize("NFD").toLowerCase().replace(/[\u0300-\u036f]/g, "").split(/[\W_-]+/);
	xs.forEach(x => {
		if(x.length <= 3) { return; }
		if(FREQUENT_WORDS[x]) { return; }
		if(!x.match(/^[0-9]008$/) && x.match(/^[0-9]{4,}$/)) { return; }
		words[x] = (words[x] || 0) + 1;
	});
}